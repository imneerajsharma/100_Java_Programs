import java.util.Scanner;
public class enhance_loops {

    public static void main (String [] args){

        int numbers[]= {01,21,34,43,59,64,75,89};
        String languages[]={"C","C++","JAVA","PYTHON","JAVASCRIPT","TYPESCRIPT","R"};
        for(int i: numbers){
            System.out.println(i);
        }

        System.out.println();
        for(String i: languages){
            System.out.println(i);
        }


    }
}
